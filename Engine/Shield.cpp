bool Shield::checkNextPos(float pos, Direction wanted) const
{
	switch (wanted)
	{
	case Direction::Left:
		if (pos - width - distance >= 0.0f)
		{
			return true;
		}
		break;

	case Direction::Right:
		if (pos + width + distance < Graphics::ScreenWidth)
		{
			return true;
		}
		break;

	default:
		break;
	}
	return false;
}